<script setup>
import MicrophoneSettings from '~/components/MicrophoneSettings.vue'
import MicrophonePermissionModal from '~/components/MicrophonePermissionModal.vue'

const { isInRoom, room, gameData, player, isHost, forceStopVoiceActivity, forceStartVoiceActivity, clearRoom, getAllRoles, sendVoiceActivity } = useGame()
const { isConnected } = useSocket()
const { soundsEnabled, toggleSounds } = useSounds()
const { microphoneEnabled, toggleMicrophone, retryMicrophoneEnable } = useVoiceActivity()
const { username, hasUsername } = useUser()

const showRules = ref(false)
const showRolesGuide = ref(false)
const showUsernameModal = ref(false)
const showMicSettings = ref(false)
const showMicPermissionModal = ref(false)

// Refs
const micPermissionModal = ref(null)

// Microphone settings logic
let micHideTimeout = null

const handleMicMouseLeave = () => {
  micHideTimeout = setTimeout(() => {
    showMicSettings.value = false
  }, 200) // Small delay to allow moving to settings panel
}

const cancelMicHide = () => {
  if (micHideTimeout) {
    clearTimeout(micHideTimeout)
    micHideTimeout = null
  }
}

const onMicSettingsApplied = () => {
  console.log('‚úÖ –ù–∞—Å—Ç—Ä–æ–π–∫–∏ –º–∏–∫—Ä–æ—Ñ–æ–Ω–∞ –ø—Ä–∏–º–µ–Ω–µ–Ω—ã')
}

const roomId = computed(() => room.id)
const gameRoles = computed(() => gameData.selectedRoles || [])
const playerRole = computed(() => player.role)
const allRoles = computed(() => {
  // –ï—Å–ª–∏ —Ä–æ–ª–∏ –µ—Å—Ç—å –≤ gameData, –∏—Å–ø–æ–ª—å–∑—É–µ–º –∏—Ö
  if (gameData.roles && Object.keys(gameData.roles).length > 0) {
    return gameData.roles
  }
  
  // –ò–Ω–∞—á–µ –ø–æ–ª—É—á–∞–µ–º —Ä–æ–ª–∏ —á–µ—Ä–µ–∑ getAllRoles()
  const roles = getAllRoles()
  return roles || {}
})

// –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –¥–ª—è –∫–Ω–æ–ø–∫–∏ –º–∏–∫—Ä–æ—Ñ–æ–Ω–∞ —Å –ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω—ã–º–∏ —Ñ—É–Ω–∫—Ü–∏—è–º–∏
const handleToggleMicrophone = async (event) => {
  console.log('üîÑ Toggling microphone from layout')
  
  // –ï—Å–ª–∏ –º–∏–∫—Ä–æ—Ñ–æ–Ω –≤–∫–ª—é—á–µ–Ω, –ø—Ä–æ—Å—Ç–æ –≤—ã–∫–ª—é—á–∞–µ–º
  if (microphoneEnabled.value) {
    await toggleMicrophone(forceStopVoiceActivity, forceStartVoiceActivity, null, event)
    return
  }
  
  // –ï—Å–ª–∏ –º–∏–∫—Ä–æ—Ñ–æ–Ω –≤—ã–∫–ª—é—á–µ–Ω, –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –º–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ –∏ –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —Ä–∞–∑—Ä–µ—à–µ–Ω–∏–µ
  showMicPermissionModal.value = true
  
  // –°–æ–∑–¥–∞–µ–º callback –¥–ª—è –≥–æ–ª–æ—Å–æ–≤–æ–π –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏
  const voiceActivityCallback = (isActive) => {
    sendVoiceActivity(isActive)
  }
  
  try {
    const result = await toggleMicrophone(forceStopVoiceActivity, forceStartVoiceActivity, voiceActivityCallback, event)
    
    if (result) {
      // –†–∞–∑—Ä–µ—à–µ–Ω–∏–µ –ø–æ–ª—É—á–µ–Ω–æ
      micPermissionModal.value?.handlePermissionGranted()
    } else {
      // –†–∞–∑—Ä–µ—à–µ–Ω–∏–µ –Ω–µ –ø–æ–ª—É—á–µ–Ω–æ
      micPermissionModal.value?.handlePermissionDenied()
    }
  } catch (error) {
    console.error('‚ùå Microphone permission error:', error)
    micPermissionModal.value?.handlePermissionError(error)
  }
}

// –û–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –æ–∫–Ω–∞ —Ä–∞–∑—Ä–µ—à–µ–Ω–∏–π
const onMicPermissionGranted = () => {
  console.log('‚úÖ Microphone permission granted')
  setTimeout(() => {
    showMicPermissionModal.value = false
  }, 2000) // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —É—Å–ø–µ—Ö 2 —Å–µ–∫—É–Ω–¥—ã
}

const onMicPermissionDenied = () => {
  console.log('‚ùå Microphone permission denied')
  // –ú–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ –æ—Å—Ç–∞–µ—Ç—Å—è –æ—Ç–∫—Ä—ã—Ç—ã–º –¥–ª—è –ø–æ–∫–∞–∑–∞ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–π
}

const retryMicrophonePermission = async (event) => {
  console.log('üîÑ Retrying microphone permission')
  
  const voiceActivityCallback = (isActive) => {
    sendVoiceActivity(isActive)
  }
  
  try {
    const result = await retryMicrophoneEnable(voiceActivityCallback, event)
    
    if (result) {
      micPermissionModal.value?.handlePermissionGranted()
    } else {
      micPermissionModal.value?.handlePermissionDenied()
    }
  } catch (error) {
    console.error('‚ùå Microphone retry error:', error)
    micPermissionModal.value?.handlePermissionError(error)
  }
}

const leaveRoom = async () => {
  if (confirm('–í—ã —É–≤–µ—Ä–µ–Ω—ã, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ –ø–æ–∫–∏–Ω—É—Ç—å –∫–æ–º–Ω–∞—Ç—É?')) {
    // –ó–∞–∫—Ä—ã–≤–∞–µ–º –≤—Å–µ –º–æ–¥–∞–ª—å–Ω—ã–µ –æ–∫–Ω–∞
    showRolesGuide.value = false
    showRules.value = false
    showUsernameModal.value = false
    
    // –û—á–∏—â–∞–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∏–≥—Ä—ã
    clearRoom()
    
    // –ù–∞–≤–∏–≥–∏—Ä—É–µ–º –Ω–∞ –≥–ª–∞–≤–Ω—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É –∏—Å–ø–æ–ª—å–∑—É—è Nuxt –Ω–∞–≤–∏–≥–∞—Ü–∏—é
    await navigateTo('/')
  }
}

// –û—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏–µ –∏–∑–º–µ–Ω–µ–Ω–∏–π –º–∞—Ä—à—Ä—É—Ç–∞ –¥–ª—è –æ—á–∏—Å—Ç–∫–∏ —Å–æ—Å—Ç–æ—è–Ω–∏—è
const route = useRoute()
watch(() => route.path, (newPath, oldPath) => {
  // –ï—Å–ª–∏ –ø–µ—Ä–µ—Ö–æ–¥–∏–º —Å –∏–≥—Ä–æ–≤–æ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã –Ω–∞ –≥–ª–∞–≤–Ω—É—é –∏–ª–∏ –¥—Ä—É–≥—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É
  if (oldPath?.startsWith('/game/') && !newPath.startsWith('/game/')) {
    console.log('üîÑ Route changed from game to non-game page, clearing room state')
    clearRoom()
    
    // –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ –æ—á–∏—â–∞–µ–º –º–æ–¥–∞–ª—å–Ω—ã–µ –æ–∫–Ω–∞
    showRolesGuide.value = false
    showRules.value = false
    showUsernameModal.value = false
  }
}, { immediate: false })

// –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–ª–∞–≤–∏—à–∏ Escape –¥–ª—è –∑–∞–∫—Ä—ã—Ç–∏—è –º–æ–¥–∞–ª—å–Ω—ã—Ö –æ–∫–æ–Ω
onMounted(() => {
  const handleEscape = (event) => {
    if (event.key === 'Escape') {
      if (showUsernameModal.value) {
        showUsernameModal.value = false
      } else if (showRules.value) {
        showRules.value = false
      } else if (showRolesGuide.value) {
        showRolesGuide.value = false
      }
    }
  }
  
  window.addEventListener('keydown', handleEscape)
  
  // –û—á–∏—Å—Ç–∫–∞ —Å–æ—Å—Ç–æ—è–Ω–∏—è –ø—Ä–∏ –ø–µ—Ä–≤–æ–º –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏, –µ—Å–ª–∏ –Ω–µ –Ω–∞—Ö–æ–¥–∏–º—Å—è –≤ –∏–≥—Ä–æ–≤–æ–π –∫–æ–º–Ω–∞—Ç–µ
  const currentPath = route.path
  if (!currentPath.startsWith('/game/') && isInRoom.value) {
    console.log('üßπ Page loaded outside game, clearing stale room state')
    clearRoom()
  }
  
  // –ü—Ä–µ–¥–∑–∞–≥—Ä—É–∂–∞–µ–º —Ä–æ–ª–∏ –¥–ª—è —Å–ø—Ä–∞–≤–æ—á–Ω–∏–∫–∞
  const loadRoles = async () => {
    try {
      const roles = await getAllRoles()
      console.log('Roles loaded for guide:', Object.keys(roles || {}).length)
    } catch (error) {
      console.error('Failed to load roles:', error)
    }
  }
  
  loadRoles()
  
  onUnmounted(() => {
    window.removeEventListener('keydown', handleEscape)
  })
})
</script>

